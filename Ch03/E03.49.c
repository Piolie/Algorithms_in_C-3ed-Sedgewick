/* ---------------------------------------------------------------------
                             EXERCISE 3.49
------------------------------------------------------------------------
Write a program that frees the nodes in even positions in a linked list
(the second, fourth, sixth, and so forth).
--------------------------------------------------------------------- */

/* ---------------------------------------------------------------------
This exercise is identical to 3.48, we just change M for 2.
--------------------------------------------------------------------- */

#include "list.h"
#include <stdio.h>
#include <stdlib.h>

#define N 100
#define M 2

int main(void) {
  int i;
  link head = malloc(sizeof *head);
  head->next = NULL;
  link t;

  printf("Unsorted nodes:\n");
  for (i = 0, t = head; i < N; i++) {
    t->next = malloc(sizeof *t);
    t = t->next;
    t->next = NULL;
    t->item = rand() % 1000;
    printf("%3d ", t->item);
  }

  // free every Mth node
  link temp;
  i = 1;
  for (t = head->next; t != NULL; t = t->next) {
    if (i % (M - 1) == 0) {
      temp = t->next;
      if (temp) {
        t->next = temp->next;
        free(temp);
      } else
        t->next = NULL;
    }
    i++;
  }

  printf("\n\nPruned list:\n");
  for (t = head->next; t != NULL; t = t->next)
    printf("%3d ", t->item);
}

