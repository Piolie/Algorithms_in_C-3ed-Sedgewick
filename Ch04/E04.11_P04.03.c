/* ---------------------------------------------------------------------
                             EXERCISE 4.11
------------------------------------------------------------------------
Extend Programs 4.2 and 4.3 to include the - (subtract) and / (divide)
operations.
--------------------------------------------------------------------- */

#include "STACK.h"
#include <stdio.h>
#include <string.h>

int main(int argc, char *argv[]) {
  (void)argc;
  char *a = argv[1];
  int i, N = strlen(a);
  STACKinit(N);
  for (i = 0; i < N; i++) {
    if (a[i] == ')')
      printf("%c ", STACKpop());
    if ((a[i] == '+') || (a[i] == '*') || (a[i] == '-') || (a[i] == '/'))
      STACKpush(a[i]);
    if ((a[i] >= '0') && (a[i] <= '9'))
      printf("%c ", a[i]);
  }
  printf("\n");
}

