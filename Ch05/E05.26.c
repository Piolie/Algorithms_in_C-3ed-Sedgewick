/* ---------------------------------------------------------------------
                             EXERCISE 5.26
------------------------------------------------------------------------
Draw the results of using the recursive ruler-drawing program
(Program 5.8) for these unintended values of the arguments:
rule(0, 11, 4), rule(4, 20, 4), and rule (7, 30, 5).
--------------------------------------------------------------------- */

#include <stdio.h>
#include <stdlib.h>

void mark(int m, int h) {
  (void)m;
  printf("%2d ", h);
}

void rule(int l, int r, int h) {
  int m = (l + r) / 2;
  if (h > 0) {
    rule(l, m, h - 1);
    mark(m, h);
    rule(m, r, h - 1);
  }
}

int main(void) {
  rule(0, 11, 4);
  printf("\n");

  rule(4, 20, 4);
  printf("\n");

  rule(7, 30, 5);
}

